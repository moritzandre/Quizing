export const STRINGS = { en, de };

/**
 * Translate a key for a language, interpolating {placeholders} from `vars`.
 * Falls back to English, then to the key itself.
 */
export function translate(lang, key, vars) {
  const s = (STRINGS[lang] && STRINGS[lang][key]) ?? STRINGS.en[key] ?? key;
  if (!vars) return s;
  return s.replace(/\{(\w+)\}/g, (m, k) => (vars[k] != null ? String(vars[k]) : m));
}
